
#include<iostream>
using namespace std;

int main()
{
    int n;
    cout<<"Enter the Number :";
    cin>>n;
    if(n==1)
    {
        cout<<"Monday";
    }
    else if(n==2)
    {
        cout<<"Tuesday";
    }
    else if(n==3)
    {
        cout<<"Wed";
    }
    else if(n==4)
    {
        cout<<"Thu";
    }
    else if(n==5)
    {
        cout<<"Fri";
    }
    else if(n==6)
    {
        cout<<"Sat";
    }
    else
    {
        cout<<"Sun";
    }
    return 0;
}




